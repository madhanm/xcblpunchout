package com.xcblpunchout.sebo;

public class PartRequest {

	private String reqPartNumber;
	private String formattedPartNumber;
	private String reqPartQty;
	
	public String getReqPartNumber() {
		return reqPartNumber;
	}
	public void setReqPartNumber(String reqPartNumber) {
		this.reqPartNumber = reqPartNumber;
	}
	public String getFormattedPartNumber() {
		return formattedPartNumber;
	}
	public void setFormattedPartNumber(String formattedPartNumber) {
		this.formattedPartNumber = formattedPartNumber;
	}
	public String getReqPartQty() {
		return reqPartQty;
	}
	public void setReqPartQty(String reqPartQty) {
		this.reqPartQty = reqPartQty;
	}
	
	
	
}
